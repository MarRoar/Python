# -- coding: utf-8 --
# 矩形
import turtle
t = turtle.Pen()

t.width(3)
t.color('#712e2e')
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)

t.penup()
t.goto(110, 0)
t.pendown()

t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)

t.penup()
t.goto(0, -100)
t.pendown()

t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)

t.penup()
t.goto(110, -100)
t.pendown()

t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)
t.forward(80)
t.left(90)

turtle.done()