'''
    逻辑运算符
    or      x or y      x 为 True 则返回True
                        x 为 False 则返回 y
    and    x and y      x 为 True, 则返回 y 的值
                        x 为 False 则返回 False
    not     not x       x 为 True 则返回 False
                        x 为 False 则返回 True
'''
print(True or 12)
print(False or 12)

print(True and 32)
print(False and 32)